export type Lang = "bn" | "en";
export const DEFAULT_LANG: Lang = "bn";

const BN_DIGITS = ["০", "১", "২", "৩", "৪", "৫", "৬", "৭", "৮", "৯"];

/** Localize digits: Bengali digits in bn, plain in en. */
export function num(value: number | string, lang: Lang): string {
  const s = value.toString();
  return lang === "bn" ? s.replace(/\d/g, (d) => BN_DIGITS[Number(d)]) : s;
}

export function money(value: number, lang: Lang): string {
  return "৳" + num(Math.round(value).toLocaleString("en-US"), lang);
}

export function dateFmt(
  date: string | Date,
  lang: Lang,
  opts: Intl.DateTimeFormatOptions,
): string {
  const d = typeof date === "string" ? new Date(date) : date;
  return d.toLocaleString(lang === "bn" ? "bn-BD" : "en-US", opts);
}

